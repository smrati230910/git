public class swapnumber {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		int c=0;
		
		
		System.out.println("value of a"  +a);
		System.out.println("value of b" +b);
		c=a; //c=10
		a=b; //
		b=c;
		
		System.out.println("value of a" +a);
		System.out.println("value of b" +b);
		
		

	}

}
 